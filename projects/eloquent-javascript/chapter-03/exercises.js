// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(x,y) { //prints the smallest number out of the two parameters
  if(x < y){
    return x;
  }else if(x > y){
    return y;
  }else{
    return x || y;
  }

}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(x) { //checks to see if number is even
  if(x % 2 === 0){
    return true;
  }else{
    return false;
  }

}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, ch) {
  let counted = 0; // setting a counter 
  for (let i = 0; i < string.length; i++) { //when a letter matches our character , we will add 1 to out counter 
    if (string[i] == ch) {
      counted += 1;
    }
  }
  return counted; // we return what we counted
}
////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  return countChars(string, 'B'); // using our countChars function and passing our string and 'B', as we are checking for the letter b.
  

}

